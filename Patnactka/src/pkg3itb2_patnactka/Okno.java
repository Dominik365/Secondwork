    private JButton[][] tlacitka=new JButton[4][4]; //inicialiyovane pole, musime ale inicializovat jednotliva tlacitka
    
    public Okno(logikahtHry logika) {
        initComponents();
        this.logika=logika;
        PridejTlacitka();
        NaplnTlacitkaHodnotamaZPole();
        PridejUdalostiTlacitek();
    }
    
    private void PridejTlacitka(){
        for (int i = 0; i < tlacitka.length; i++) {
            for (int j = 0; j < tlacitka[0].length; j++) {
                tlacitka[i][j]=new JButton();
                tlacitka[i][j].setSize(50,50);
                tlacitka[i][j].setVisible(true);
                this.add(tlacitka[i][j]);
                tlacitka[i][j].setLocation(j*50, i*50);
                tlacitka[i][j].setName(i+","+j); //ulozime si souradnice tlacitka v poli
            }
        }
    }
    
    private void PridejUdalostiTlacitek(){
        for (int i = 0; i < tlacitka.length; i++) {
            for (int j = 0; j < tlacitka[0].length; j++) {
                tlacitka[i][j].addMouseListener(new MouseAdapter() {
                    @Override
                    public void mouseClicked(MouseEvent me) {
                        //toto se provede pri stisku tlacitka
                        System.out.println("klik");
                        //musime poslat info o tom na jake tlacitko v poli jsme kliknuli
                        // souradnice si ulozime do vlastnosti Name
                        
                        ////// ziskani tlacitka co vyvolalo udalost
                        JButton pomocneTl=(JButton)(me.getSource()); //objekt ktery sme ziskali z me.getSource() jsem pretzpovali na JButton
                        /// ziskani souradnic z Name tlacitka
                        String[] souradniceVeStringu=pomocneTl.getName().split(","); //prvni prvek v poli odpovida souradnici i a druhy prvek odpovida souradnici j
                        
                        logika.PresunCislo(Integer.parseInt(souradniceVeStringu[0]), Integer.parseInt(souradniceVeStringu[1]));
                        if(logika.Vyhodnot()){
                            JOptionPane.showMessageDialog(null,"VyhrÃ¡l jsi!");  
                        }
                        NaplnTlacitkaHodnotamaZPole();
                        
                        super.mouseClicked(me); //To change body of generated methods, choose Tools | Templates.
                    }
                    
                
                });
            
            }
        }
    }
    
    private void NaplnTlacitkaHodnotamaZPole(){
        for (int i = 0; i < tlacitka.length; i++) {
            for (int j = 0; j < tlacitka[0].length; j++) {
                tlacitka[i][j].setText(logika.getHodnotyPole()[i][j]+"");
                if(logika.getHodnotyPole()[i][j]==0){
                    //prazdne policko
                    tlacitka[i][j].setText("");
                }
            }
        }
    }
    

